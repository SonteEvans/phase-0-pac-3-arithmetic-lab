function add(a, b) {
    return a + b
}

function subtract(c, d) {
    return c - d
}

function multiply(e, f) {
    return e * f
}

function divide(g, h) {
    return g / h
}

function increment(q) {
    return (q + 1)
}

function decrement(x) {
    return (x - 1)
}

function makeInt(z) {
    
    return parseFloat(z)
}

function preserveDecimal(j) {
    return parseFloat(j)
}